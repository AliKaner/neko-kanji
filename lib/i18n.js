const I18nContext = createContext(null);

const JP_FONTS = ["round", "sharp", "play"];

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState("tr");
  const [jpFont, setJpFontState] = useState("round");

  useEffect(() => {
    const saved = window.localStorage.getItem("lang");
    if (saved === "en" || saved === "tr") setLangState(saved);
    const savedFont = window.localStorage.getItem("jpfont");
    if (JP_FONTS.includes(savedFont)) {
      setJpFontState(savedFont);
      document.documentElement.dataset.jpfont = savedFont;
    }
  }, []);

  const setLang = (l) => {
    setLangState(l);
    window.localStorage.setItem("lang", l);
  };

  const setJpFont = (f) => {
    if (!JP_FONTS.includes(f)) return;
    setJpFontState(f);
    window.localStorage.setItem("jpfont", f);
    document.documentElement.dataset.jpfont = f;
  };

  const t = (key, vars) => {
    const str = STRINGS[lang][key] ?? STRINGS.tr[key] ?? key;
    if (!vars) return str;
    return str.replace(/\{(\w+)\}/g, (_, k) => vars[k] ?? "");
  };

  return (
    <I18nContext.Provider value={{ lang, setLang, t, jpFont, setJpFont }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
